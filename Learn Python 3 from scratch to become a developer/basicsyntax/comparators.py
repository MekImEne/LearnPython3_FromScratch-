"""
==  --> Value equality
!= --> Not equal to
< --> Less than
> --> Greater than
<= --> Less than or equal to
>= --> Greater than or equal to
>= --> Greater than or equal to
"""

bool_one = 10 == 10
bool_two = 10 == 11
print(bool_one)
print(bool_two)

not_equal = 10 != 10
print(not_equal)

less_than = 10 < 10
print(less_than)

greater_than = 10 > 11
print(greater_than)

lt_eq = 10 <= 10
print(lt_eq)

gt_eq = 11 >= 11-1
print(gt_eq)

